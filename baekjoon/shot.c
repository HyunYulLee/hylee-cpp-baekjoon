#ifdef _MSC_VER
#define _CRT_SECURE_NO_WARNINGS
#endif 

#include <stdio.h>

int main() {
    int n, m, i = 0, j, d = 0, c = 0, a[5555];
    
    scanf("%d%d", &n, &m);
    
    for (; i < n; d && printf(","), printf("%c%d", (d = 1) && i++ ? ' ' : '<', a[c] = c ? c : n)) {
        for (j = 0; j < m; a[c = ++c % n] || j++);
    }
    
    printf(">");
    
    return 0;
}
